    <h3>¿Arte o provocación?</h3>

    <p>Escurridizo, provocador, misterioso… Banksy, el artista británico cuya identidad aún se desconoce, está considerado como <strong>uno de los principales exponentes del Street Art contemporáneo</strong>. Sus obras, a menudo satíricas, abordan temas universales como la política, la cultura o la ética. El aura de misterio que, por elección y por necesidad, se perpetúa cada vez que se menciona a Banksy, lo ha convertido en una figura mítica de nuestro tiempo.</p>

    <p>Banksy emplea una forma de comunicación directa, rechazando el sistema y sus reglas. El artista se dirige a su público sin ningún filtro, y sus obras son textos visuales capaces de informar e inspirar la reflexión. Guerra, riqueza y pobreza, animales, globalización, consumismo, política, poder y ambientalismo… los temas que Banksy explora son los temas del mundo en que vivimos.</p>

    <p>Desde las trincheras del asfalto, Banksy ha desafiado las reglas del juego del arte contemporáneo. Su protesta visual ha conectado con un público enorme y heterogéneo y hoy en día es <strong>uno de los artistas más reconocidos y admirados entre los amantes del arte y también de las generaciones más jóvenes</strong>.</p>

    <p>En palabras de <strong>Alexander Nachkebiya, comisario de la Exposición</strong>: «Banksy ha adquirido la categoría de fenómeno y es uno de los artistas más brillantes e importantes de nuestro tiempo. Su trabajo es un desafío para el sistema, una protesta, una marca extremadamente bien construida, un misterio, una desobediencia a la ley… Queremos que cada visitante de esta exposición pueda resolver por sí mismo quién es realmente Banksy: ¿un genio o un gamberro?, ¿un artista o un empresario?, ¿un provocador o un rebelde? Esta nueva exposición pretende mostrar la profundidad del talento de Banksy, sus múltiples capas y dimensiones para que sean los propios visitantes quienes piensen y decidan. Su trabajo, siempre actual y muy completo, profundiza en el alma de cada uno de nosotros. Supongo que todo esto lo convierte en un genio para mí.”</p>

