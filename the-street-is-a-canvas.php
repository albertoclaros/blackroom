    <p>BANKSY. The Street is a Canvas nos acerca al controvertido universo artístico de uno de los creadores más influyentes de los últimos años, a través de diferentes ámbitos temáticos y más de 50 creaciones que incluyen una selección de obras únicas ejecutadas con diferentes técnicas: óleo o acrílico sobre lienzo, spray sobre lienzo, serigrafías de edición limitada, esténciles sobre metal u hormigón, esculturas, instalaciones, vídeos y fotografías. Gran parte de las piezas, procedentes de colecciones privadas internacionales se exhiben en Madrid por primera vez.</p>

    <p>Una instalación multimedia envolvente especialmente creada para esta muestra dará la bienvenida al visitante, desvelando pistas sobre el misterioso artista, destacando sus piezas más importantes y enmarcando su insólita trayectoria, no exenta de polémica. Entre las obras más reconocidas de la muestra se encuentra la serigrafía original de la serie «Niña con globo», similar a la destruida por el propio artista en una acción sin precedente en Sotheby’s, la casa de subastas de Londres.</p>

